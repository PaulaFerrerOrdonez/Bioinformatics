# Introducción a variables y tipos de datos en Python

# Variables numéricas
edad = 25
peso = 70.5

peso_redondeado=int(peso)

# Variables de texto
nombre = "Sara"
genoma = "ATCGTTAG"

# Variables booleanas
es_estudiante = True

print("Nombre:", nombre)
print("Edad:", edad)
print("Peso:", peso)
print("Genoma:", genoma)
print("¿Es estudiante?", es_estudiante)
print("Peso redondeado:", peso_redondeado)

# Operaciones simples
suma = edad + 5
longitud_genoma = len(genoma)

print("Edad + 5:", suma)
print("Longitud del genoma:", longitud_genoma)